/*
*
* Place this code snippet inside your jQuery ready function. It will take an anchor tag with an id
* and do a smooth scroll to that position in the DOM
* example tag: <a href="#contact">Contact Us</a>
*/

$('a[href*=#]:not([href=#])').click(function() {
    if (location.pathname.replace(/^\//,'') == this.pathname.replace(/^\//,'') 
        || location.hostname == this.hostname) {

        var target = $(this.hash);
        target = target.length ? target : $('[name=' + this.hash.slice(1) +']');
           if (target.length) {
             $('html,body').animate({
                 scrollTop: target.offset().top
            }, 1000);
            return false;
        }
    }
});
